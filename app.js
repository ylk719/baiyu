// ============== 评价页面 ==============
function ReviewPage() {
  const { state, navigate, pageParams, showToast } = useApp();
  const orderId = pageParams.orderId;
  const order = state.orders.find(o => o.id === orderId);
  const [rating, setRating] = useState(5);
  const [content, setContent] = useState('');
  const [tags, setTags] = useState([]);

  const availableTags = ['味道不错', '分量足', '配送快', '包装精美', '性价比高', '推荐菜品'];

  const toggleTag = (tag) => {
    if (tags.includes(tag)) {
      setTags(tags.filter(t => t !== tag));
    } else {
      setTags([...tags, tag]);
    }
  };

  const handleSubmit = () => {
    if (!content.trim()) {
      showToast('请输入评价内容');
      return;
    }
    showToast('评价提交成功');
    setTimeout(() => navigate('orderDetail', { orderId }), 1000);
  };

  if (!order) {
    return (
      <div className="review-page">
        <div className="nav-header" style={{ background: '#fff', borderBottom: '1px solid #f0f0f0' }}>
          <div className="back-btn" onClick={() => navigate('orders')}>←</div>
          <div className="nav-title">写评价</div>
          <div style={{ width: 28 }}></div>
        </div>
        <div className="empty-state" style={{ paddingTop: 100 }}>
          <div className="empty-icon">📋</div>
          <div className="empty-text">订单不存在</div>
        </div>
      </div>
    );
  }

  return (
    <div className="review-page">
      <div className="nav-header" style={{ background: '#fff', borderBottom: '1px solid #f0f0f0' }}>
        <div className="back-btn" onClick={() => navigate('orderDetail', { orderId })}>←</div>
        <div className="nav-title">写评价</div>
        <div style={{ width: 28 }}></div>
      </div>

      <div className="review-store-info">
        <div className="review-store-avatar">{order.storeAvatar}</div>
        <div className="review-store-name">{order.storeName}</div>
      </div>

      <div className="review-section">
        <div className="review-section-title">评分</div>
        <div className="review-stars">
          {[1, 2, 3, 4, 5].map(star => (
            <span
              key={star}
              className={`review-star ${star <= rating ? 'active' : ''}`}
              onClick={() => setRating(star)}
            >
              ★
            </span>
          ))}
          <span className="review-rating-text">
            {rating === 5 ? '非常满意' : rating === 4 ? '满意' : rating === 3 ? '一般' : rating === 2 ? '不满意' : '非常不满意'}
          </span>
        </div>
      </div>

      <div className="review-section">
        <div className="review-section-title">评价标签</div>
        <div className="review-tags">
          {availableTags.map(tag => (
            <span
              key={tag}
              className={`review-tag ${tags.includes(tag) ? 'active' : ''}`}
              onClick={() => toggleTag(tag)}
            >
              {tag}
            </span>
          ))}
        </div>
      </div>

      <div className="review-section">
        <div className="review-section-title">评价内容</div>
        <textarea
          className="review-textarea"
          placeholder="说说您的用餐体验吧~"
          value={content}
          onChange={e => setContent(e.target.value)}
          maxLength={500}
        />
        <div className="review-char-count">{content.length}/500</div>
      </div>

      <div className="review-items">
        <div className="review-section-title">评价商品</div>
        {order.items.map((item, i) => (
          <div key={i} className="review-goods-item">
            <span className="review-goods-emoji">{item.emoji}</span>
            <span className="review-goods-name">{item.name}</span>
          </div>
        ))}
      </div>

      <div className="review-bottom">
        <button className="review-submit-btn" onClick={handleSubmit}>提交评价</button>
      </div>
    </div>
  );
}

// ============== 个人中心 ==============
function ProfilePage() {
  const { navigate, state, updateProfile, showToast } = useApp();
  const avatarInputRef = useRef(null);
  const bgInputRef = useRef(null);
  const [showNameModal, setShowNameModal] = useState(false);
  const [editName, setEditName] = useState(state.profile.name);

  const menuItems = [
    { icon: '❤️', text: '我的收藏', onClick: () => navigate('favorites') },
    { icon: '🎫', text: '优惠券', onClick: () => navigate('coupons') },
    { icon: '📍', text: '地址管理', onClick: () => navigate('addressList') },
    { icon: '⚙️', text: '设置', onClick: () => navigate('settings') }
  ];

  const handleAvatarUpload = (e) => {
    const file = e.target.files[0];
    if (file) {
      const reader = new FileReader();
      reader.onload = (ev) => {
        updateProfile({ avatarImage: ev.target.result });
      };
      reader.readAsDataURL(file);
    }
  };

  const handleBgUpload = (e) => {
    const file = e.target.files[0];
    if (file) {
      const reader = new FileReader();
      reader.onload = (ev) => {
        updateProfile({ backgroundImage: ev.target.result });
      };
      reader.readAsDataURL(file);
    }
  };

  const handleSaveName = () => {
    if (editName.trim()) {
      updateProfile({ name: editName.trim() });
      setShowNameModal(false);
    } else {
      showToast('昵称不能为空');
    }
  };

  const headerStyle = state.profile.backgroundImage
    ? { backgroundImage: `url(${state.profile.backgroundImage})`, backgroundSize: 'cover', backgroundPosition: 'center' }
    : {};

  return (
    <div className="profile-page">
      <div className="profile-header" style={headerStyle} onClick={() => bgInputRef.current && bgInputRef.current.click()}>
        <input
          ref={bgInputRef}
          type="file"
          accept="image/*"
          style={{ display: 'none' }}
          onChange={handleBgUpload}
        />
        <div className="profile-avatar-wrap" onClick={(e) => { e.stopPropagation(); avatarInputRef.current && avatarInputRef.current.click(); }}>
          <input
            ref={avatarInputRef}
            type="file"
            accept="image/*"
            style={{ display: 'none' }}
            onChange={handleAvatarUpload}
          />
          {state.profile.avatarImage ? (
            <img src={state.profile.avatarImage} alt="头像" className="profile-avatar-img" />
          ) : (
            <div className="profile-avatar">{state.profile.avatar}</div>
          )}
          <div className="avatar-edit-icon">📷</div>
        </div>
        <div className="profile-info" onClick={(e) => { e.stopPropagation(); setShowNameModal(true); setEditName(state.profile.name); }}>
          <div className="profile-name">
            {state.profile.name}
            <span className="profile-edit-icon">✏️</span>
          </div>
          <div className="profile-phone">{state.profile.phone}</div>
        </div>
        <div className="profile-bg-hint">点击更换背景</div>
      </div>

      <div className="order-section">
        <div className="order-section-title">
          <span>我的订单</span>
          <span className="order-all" onClick={() => navigate('orders')}>全部订单 ›</span>
        </div>
        <div className="order-icons">
          <div className="order-icon-item" onClick={() => navigate('orders', { tab: 'pending' })}>
            <span className="order-icon">💰</span>
            <span>待付款</span>
          </div>
          <div className="order-icon-item" onClick={() => navigate('orders', { tab: 'processing' })}>
            <span className="order-icon">🚴</span>
            <span>配送中</span>
          </div>
          <div className="order-icon-item" onClick={() => navigate('orders', { tab: 'completed' })}>
            <span className="order-icon">✅</span>
            <span>已完成</span>
          </div>
          <div className="order-icon-item" onClick={() => navigate('orders', { tab: 'refund' })}>
            <span className="order-icon">↩️</span>
            <span>退款</span>
          </div>
        </div>
      </div>

      <div className="menu-list">
        {menuItems.map((item, i) => (
          <div key={i} className="menu-item-row" onClick={item.onClick}>
            <span className="menu-item-icon">{item.icon}</span>
            <span className="menu-item-text">{item.text}</span>
            <span className="menu-item-arrow">›</span>
          </div>
        ))}
      </div>

      {showNameModal && (
        <div className="modal-overlay" onClick={() => setShowNameModal(false)}>
          <div className="modal-content" onClick={e => e.stopPropagation()}>
            <div className="modal-close" onClick={() => setShowNameModal(false)}>✕</div>
            <div className="modal-title">修改昵称</div>
            <input
              type="text"
              className="modal-input"
              value={editName}
              onChange={(e) => setEditName(e.target.value)}
              placeholder="请输入新昵称"
              maxLength={20}
            />
            <button className="modal-btn" onClick={handleSaveName}>保存</button>
          </div>
        </div>
      )}
    </div>
  );
}

