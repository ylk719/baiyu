// ============== 设置页 ==============
function SettingsPage() {
  const { navigate, exportData, importData, showToast, setState } = useApp();
  const fileInputRef = useRef(null);

  const handleImport = (e) => {
    const file = e.target.files[0];
    if (file) {
      const reader = new FileReader();
      reader.onload = (event) => {
        importData(event.target.result);
      };
      reader.readAsText(file);
    }
  };

  const handleClearData = () => {
    if (confirm('确定要清除所有数据吗？此操作不可恢复！')) {
      localStorage.removeItem(STORAGE_KEY);
      setState(getDefaultData());
      showToast('数据已清除');
    }
  };

  const menuItems = [
    { icon: '📤', text: '导出数据', onClick: exportData },
    { icon: '📥', text: '导入数据', onClick: () => fileInputRef.current?.click() },
    { icon: '🗑️', text: '清除数据', onClick: handleClearData, danger: true }
  ];

  return (
    <div style={{ background: '#f5f5f5', minHeight: '100vh' }}>
      <div className="nav-header" style={{ background: '#fff', borderBottom: '1px solid #f0f0f0' }}>
        <div className="back-btn" onClick={() => navigate('profile')}>←</div>
        <div className="nav-title">设置</div>
        <div style={{ width: 28 }}></div>
      </div>

      <div className="menu-list" style={{ marginTop: 10 }}>
        {menuItems.map((item, i) => (
          <div key={i} className="menu-item-row" onClick={item.onClick}>
            <span className="menu-item-icon">{item.icon}</span>
            <span className="menu-item-text" style={{ color: item.danger ? '#FF4D4F' : '#333' }}>{item.text}</span>
            <span className="menu-item-arrow">›</span>
          </div>
        ))}
      </div>

      <input
        ref={fileInputRef}
        type="file"
        accept=".json"
        style={{ display: 'none' }}
        onChange={handleImport}
      />

      <div style={{ textAlign: 'center', padding: '30px 20px', color: '#ccc', fontSize: 12 }}>
        美团外卖模拟版 v1.0
      </div>
    </div>
  );
}

