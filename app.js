function PasswordModal({ amount, onClose, onConfirm }) {
  const [password, setPassword] = useState('');
  const { showToast } = useApp();

  const handleKeyPress = (num) => {
    if (password.length < 6) {
      const newPwd = password + num;
      setPassword(newPwd);
      if (newPwd.length === 6) {
        setTimeout(() => {
          onConfirm();
        }, 300);
      }
    }
  };

  const handleDelete = () => {
    setPassword(password.slice(0, -1));
  };

  const nums = ['1', '2', '3', '4', '5', '6', '7', '8', '9', '', '0', '⌫'];

  return (
    <div className="modal-overlay" onClick={onClose}>
      <div className="modal-content" onClick={e => e.stopPropagation()}>
        <div className="modal-close" onClick={onClose}>✕</div>
        <div className="modal-title">请输入支付密码</div>
        <div className="modal-amount"><small>¥</small>{amount.toFixed(2)}</div>
        <div className="password-dots">
          {[0, 1, 2, 3, 4, 5].map(i => (
            <div key={i} className={`password-dot ${password.length > i ? 'filled' : ''}`}>
              {password.length > i ? '•' : ''}
            </div>
          ))}
        </div>
        <div style={{ display: 'grid', gridTemplateColumns: 'repeat(3, 1fr)', gap: 8 }}>
          {nums.map((num, i) => (
            <button
              key={i}
              style={{
                padding: '12px 0',
                fontSize: 18,
                fontWeight: 500,
                background: num ? '#f5f5f5' : 'transparent',
                borderRadius: 8,
                color: num ? '#333' : 'transparent',
                cursor: num ? 'pointer' : 'default'
              }}
              onClick={() => {
                if (num === '⌫') handleDelete();
                else if (num) handleKeyPress(num);
              }}
            >
              {num}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}

