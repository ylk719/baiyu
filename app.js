function AddressEditPage() {
  const { pageParams, state, addAddress, updateAddress, deleteAddress, navigate, showToast } = useApp();
  const addressId = pageParams.addressId;
  const existing = state.addresses.find(a => a.id === addressId);

  const [form, setForm] = useState(existing || { name: '', phone: '', address: '', isDefault: false });

  const handleSave = () => {
    if (!form.name || !form.phone || !form.address) {
      showToast('请填写完整信息');
      return;
    }
    if (addressId) {
      updateAddress(addressId, form);
    } else {
      addAddress(form);
    }
    navigate('addressList');
  };

  const handleDelete = () => {
    if (addressId) {
      deleteAddress(addressId);
      navigate('addressList');
    }
  };

  return (
    <div style={{ background: '#f5f5f5', minHeight: '100vh', paddingBottom: 80 }}>
      <div className="nav-header" style={{ background: '#fff', borderBottom: '1px solid #f0f0f0' }}>
        <div className="back-btn" onClick={() => navigate('addressList')}>←</div>
        <div className="nav-title">{addressId ? '编辑地址' : '新增地址'}</div>
        <div style={{ width: 28 }}></div>
      </div>

      <div style={{ background: '#fff', padding: '15px' }}>
        <div className="form-group">
          <label className="form-label">收货人</label>
          <input
            className="form-input"
            placeholder="请输入姓名"
            value={form.name}
            onChange={e => setForm({ ...form, name: e.target.value })}
          />
        </div>
        <div className="form-group">
          <label className="form-label">手机号码</label>
          <input
            className="form-input"
            placeholder="请输入手机号"
            value={form.phone}
            onChange={e => setForm({ ...form, phone: e.target.value })}
          />
        </div>
        <div className="form-group">
          <label className="form-label">详细地址</label>
          <input
            className="form-input"
            placeholder="请输入详细地址"
            value={form.address}
            onChange={e => setForm({ ...form, address: e.target.value })}
          />
        </div>
        <div style={{ display: 'flex', alignItems: 'center', gap: 10, padding: '10px 0' }}>
          <div
            style={{
              width: 20,
              height: 20,
              borderRadius: 4,
              border: `2px solid ${form.isDefault ? '#FF6B00' : '#ddd'}`,
              background: form.isDefault ? '#FF6B00' : '#fff',
              display: 'flex',
              alignItems: 'center',
              justifyContent: 'center',
              color: '#fff',
              fontSize: 14
            }}
            onClick={() => setForm({ ...form, isDefault: !form.isDefault })}
          >
            {form.isDefault && '✓'}
          </div>
          <span style={{ fontSize: 14 }}>设为默认地址</span>
        </div>
      </div>

      <div style={{ position: 'fixed', bottom: 0, left: '50%', transform: 'translateX(-50%)', width: '100%', maxWidth: 480, background: '#fff', padding: '12px 15px', borderTop: '1px solid #eee', zIndex: 100 }}>
        <button
          style={{ width: '100%', background: 'linear-gradient(135deg, #FFD101, #FF9500)', color: '#333', padding: 12, borderRadius: 24, fontSize: 15, fontWeight: 600 }}
          onClick={handleSave}
        >
          保存
        </button>
        {addressId && (
          <button
            style={{ width: '100%', marginTop: 10, color: '#FF4D4F', padding: 10, fontSize: 14, background: 'none' }}
            onClick={handleDelete}
          >
            删除地址
          </button>
        )}
      </div>
    </div>
  );
}

